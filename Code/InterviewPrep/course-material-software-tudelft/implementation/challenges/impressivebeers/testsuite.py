class ResultEnum(IntEnum):
    RUN_ERROR = 0
    WRONG_ANSWER = 1
    TIMEOUT = 2
    CORRECT = 3


def run_all_tests() -> ResultEnum:
    for test_case, expected in tests:
        try:
            p = run(['python', 'solution.py'], input=test_case.strip(), stdout=PIPE,
                    timeout=timeout, encoding="ascii")
            if p.returncode != 0:
                print("Failed at:")
                print(test_case)
                return ResultEnum.RUN_ERROR
            actual = str(p.stdout).strip()
            if actual != expected:
                print("Expected")
                print(expected)
                print("But was")
                print(actual)
                print("For test")
                print(test_case)
                return ResultEnum.WRONG_ANSWER
        except TimeoutExpired:
            print("Failed at:")
            print(test_case)
            return ResultEnum.TIMEOUT
    return ResultEnum.CORRECT


